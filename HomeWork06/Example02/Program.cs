void Check(double k1, double k2, double b1, double b2)//проверка коэфициентов
{
    if (k1 == k2 && b1 != b2)
    {
        Console.WriteLine("Точки пересечения графиков линейных уравнений нет. Прямые параллельны");
    }
    else if (b1 == b2)
    {
        Console.WriteLine($"Прямые пересекаются в точке (0, {b1})");
    }
    else if (k1 == k2 && b1 == b2)
    {
        Console.WriteLine("Вы задали два одинаковых уравнения");
    }
}
void PrintImage(double[,] image)//функция для отрисовки графиков
{
    for (int rows = 0; rows < image.GetLength(0); rows++)
    {
        for (int colums = 0; colums < image.GetLength(1); colums++)
        {
            if (image[rows, colums] == 0) Console.Write($".");
            else Console.Write($"*");
        }
        Console.WriteLine();
    }
}
void FillImage(double k, double b)//заполняю массив
{
    for (int x = 0; x <= 54; x++)
    {
        double value = k * x + b;
        int y = Convert.ToInt32(Math.Round(value, 0));
        if (y >= 0 && y <= 42)
        {
            pic[Math.Abs(y - 42), x] = 1;//Math.Abs(y - 42) такой аргумент для того чтобы отображать ось Y снизу экрана
            //сделал только вывод на положительной четверти. Пока не понял как сместить центры осей координат в центр массива 
        }
    }
}
